    private void StudentCourseTable(){
        List<StudentCourse> studentcourse = studentcoursedao.getStudentData();
        studentdefaulttablemodel = new DefaultTableModel(columns, 0);
        for(StudentCourse sc : studentcourse){
            Object rows[] = {sc.getId(), sc.getStudent().getName(), sc.getCourse().getName(), sc.getMarks()};
            studentdefaulttablemodel.addRow(rows);
        }
        studentcourseTable.setModel(studentdefaulttablemodel);
    }
    
    private void StudentComboBox(){
        comboBoxModel = new DefaultComboBoxModel<>();
        List<Student> studentlist = studentdao.getAllStudent();
        for(Student student : studentlist){
            comboBoxModel.addElement(student.getName());
        }
        studentComboBox.setModel(comboBoxModel);
    }
    
    private void CourseComboBox(){
        comboBoxModel = new DefaultComboBoxModel<>();
        List<Course> courselist = coursedao.getAllCourse();
        for(Course course : courselist){
            comboBoxModel.addElement(course.getName());
        }
        courseComboBox.setModel(comboBoxModel);
    }
