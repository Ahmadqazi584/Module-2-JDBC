    private void studentcourseTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studentcourseTableMouseClicked
        // Will provide Student Course Table Object
        studentcourseid = (int) studentcourseTable.getValueAt(studentcourseTable.getSelectedRow(), 0);
        studentcourse = studentcoursedao.getStudentCourseById(studentcourseid);
        
        // Show Student Name 
        student = studentdao.getStudentById(studentcourse.getStudent().getId());
        studentComboBox.setSelectedItem(student.getName());
        
        // Show Course Name
        course = coursedao.getCourseById(studentcourse.getCourse().getId());
        courseComboBox.setSelectedItem(course.getName());
        
        // Show Marks of Course
        markfield.setValue(studentcourse.getMarks());
        
    }//GEN-LAST:event_studentcourseTableMouseClicked

