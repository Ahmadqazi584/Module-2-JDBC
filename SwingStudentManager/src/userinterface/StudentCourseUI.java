    private void insertbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertbtnActionPerformed
        // TODO add your handling code here:
        studentcourse = new StudentCourse();
        String studentname = (String) studentComboBox.getSelectedItem();
        student = studentdao.getStudentByName(studentname);
        studentcourse.setStudent(student);
        
        String coursename = (String) courseComboBox.getSelectedItem();
        course = coursedao.getCourseByName(coursename);
        studentcourse.setCourse(course);
        
        int marks = (int) markfield.getValue();
        studentcourse.setMarks(marks);
        
        studentcoursedao.addStudentCourse(studentcourse);
        StudentCourseTable();
        JOptionPane.showMessageDialog(this, "Record Inserted Successfully!");
        
    }//GEN-LAST:event_insertbtnActionPerformed

    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        // Fetch Student Id
        String studentname = (String) studentComboBox.getSelectedItem();
        student = studentdao.getStudentByName(studentname);
        //Fetch Course Id 
        String coursename = (String) courseComboBox.getSelectedItem();
        course = coursedao.getCourseByName(coursename);
        int marks = (int) markfield.getValue();
        
        studentcourse.setStudent(student);
        studentcourse.setCourse(course);
        studentcourse.setMarks(marks);
        
        studentcoursedao.updateStudentCourse(studentcourse);
        
        StudentCourseTable();
        JOptionPane.showMessageDialog(this, "Record Updated Successfully!");
        
    }//GEN-LAST:event_updatebtnActionPerformed

